using System.IO.Enumeration;
using Fgvm.Godot;
using Fgvm.Types;
using Microsoft.Extensions.Logging;

namespace Fgvm.Environment;

/// <summary>
///     Provides host OS and filesystem operations used by fgvm.
/// </summary>
public sealed record HostDirectoryEntry(string FullName, string Name, FileAttributes Attributes);

public interface IHostSystem
{
    /// <summary>
    ///     Gets the current host OS and architecture.
    /// </summary>
    SystemInfo SystemInfo { get; }

    /// <summary>
    ///     Creates or updates fgvm's stable command shim.
    /// </summary>
    /// <param name="fgvmExecutablePath">The fgvm executable path the shim should call.</param>
    /// <returns>Success, or a shim error describing why the shim could not be written.</returns>
    Result<Unit, ShimError> EnsureShim(string fgvmExecutablePath);

    /// <summary>
    ///     Creates or replaces fgvm's selected launch symlink for the supplied target.
    /// </summary>
    /// <param name="symlinkTargetPath">The executable or app bundle path to link to.</param>
    /// <returns>Success, or a symlink error describing why the link could not be created.</returns>
    Result<Unit, SymlinkError> CreateOrOverwriteShortcut(string symlinkTargetPath);

    /// <summary>
    ///     Removes fgvm-managed selected Godot symlinks.
    /// </summary>
    /// <returns>Success, or a symlink error describing why removal failed.</returns>
    Result<Unit, SymlinkError> RemoveSymbolicLinks();

    /// <summary>
    ///     Resolves the currently configured fgvm symlinks.
    /// </summary>
    /// <returns>Current symlink information, or a symlink error.</returns>
    Result<SymlinkInfo, SymlinkError> ResolveCurrentSymlinks();

    /// <summary>
    ///     Lists locally installed Godot versions.
    /// </summary>
    /// <returns>Installed release names, or a filesystem error.</returns>
    Result<string[], FileSystemError> ListInstallations();

    /// <summary>
    ///     Checks whether a file exists.
    /// </summary>
    Result<bool, FileOperationError> FileExists(string path);

    /// <summary>
    ///     Checks whether a directory exists.
    /// </summary>
    Result<bool, FileOperationError> DirectoryExists(string path);

    /// <summary>
    ///     Creates a directory if it does not exist.
    /// </summary>
    Result<Unit, FileOperationError> CreateDirectory(string path);

    /// <summary>
    ///     Reads all text from a file.
    /// </summary>
    Result<string, FileOperationError> ReadAllText(string path);

    /// <summary>
    ///     Opens a file for reading.
    /// </summary>
    Result<Stream, FileOperationError> OpenRead(string path, FileShare fileShare = FileShare.Read);

    /// <summary>
    ///     Writes all text to a file.
    /// </summary>
    Result<Unit, FileOperationError> WriteAllText(string path, string contents);

    /// <summary>
    ///     Moves a file.
    /// </summary>
    Result<Unit, FileOperationError> MoveFile(string sourcePath, string destinationPath, bool overwrite);

    /// <summary>
    ///     Deletes a file if it exists.
    /// </summary>
    Result<Unit, FileOperationError> DeleteFileIfExists(string path);

    /// <summary>
    ///     Deletes a directory if it exists.
    /// </summary>
    Result<Unit, FileOperationError> DeleteDirectoryIfExists(string path, bool recursive);

    /// <summary>
    ///     Enumerates immediate child directories.
    /// </summary>
    Result<IReadOnlyList<HostDirectoryEntry>, FileOperationError> EnumerateDirectories(string path);

    /// <summary>
    ///     Gets a directory's creation time in UTC.
    /// </summary>
    Result<DateTimeOffset, FileOperationError> GetDirectoryCreatedAtUtc(string path);

    /// <summary>
    ///     Resolves a filesystem link target.
    /// </summary>
    Result<string?, FileOperationError> ResolveLinkTarget(string path, bool returnFinalTarget);
}

